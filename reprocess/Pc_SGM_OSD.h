	Pc_SGM_OSD(const Matrix<GF2>& _G, const Matrix<GF2>& _H, int _d, int _order \
	) : TEP_generator(_G.row() <= _H.row() ? _G.row() : _H.col() - _H.row()) {

		by_generator = _G.row() <= _H.row();
		n = by_generator ? _G.col() : _H.col();

		type = 31;
		cout << "Pc_SGM_OSD(" << _order << ")" << endl;

		if (by_generator == true) {
			G = _G;
			k = G.row();
		}
		else {
			H = _H;
			k = n - H.row();
			Gs = Matrix<GF2>(k, n, 'i');
		}

		min_Hamming_distance = _d;
		order = _order;
		r_abs.resize(1, n);
		y.resize(1, n);
		c0_Gs.resize(1, n);
		ce_Gs.resize(1, n);

		// initialize to arbitary values
		w_min = 0;
		w_ce_Gs = 0;
		error_num = 0;
		G_threshold = 0;
		G_OSD_threshold = 0;

		TEP_num = 0;
		is_early_termination = false;

		sigma = 0;				// reset arbitary, need to change with the channel SNR
		ET_type = 3;			// the most efficient and non-significant performance loss ET

		/**
		 * . Take care: the parameter for Pre-computed SGM OSD deocding is set inside the class
		 * \param	num_pre_computed:		number of SGMs pre-computed
		 * \param	order_pre_computed:		order of re-processing for precomputed SGMs
		 */

		// pre-compute 3 SGM for re-encoding, and set 'order_pre_computed' to 1
		num_pre_computed = 3;
		order_pre_computed = 1;

		// set 'permutation_pre_computed' and 'G_pre_computed' by cyclic shift of 'nat' and 'G'
		permutation_pre_computed.resize(1, num_pre_computed);
		G_pre_computed.resize(1, num_pre_computed);
		Matrix<int> nat(1, n, 'N');
		for (int i = 0; i < num_pre_computed; ++i) {
			nat.col_shift_right_cir(n * i / (num_pre_computed), permutation_pre_computed[i]);
			if (by_generator == true) {
				G_pre_computed[i] = G;
				G_pre_computed[i].permute_col(permutation_pre_computed[i]);
				// G.permute_column(permutation_first, Gs);

				G_pre_computed[i].GJE_4_GF2_left(permutation_pre_computed[i]);
				//cout << "Gs" << Gs;
			}
			else {
				Hs = H;
				Hs.permute_col(permutation_pre_computed[i]);

				Hs.GJE_4_GF2_right(permutation_pre_computed[i]);

				// set Gs = [I_k | P^T]
				G_pre_computed[i] = Matrix<GF2>(k, n, 'i');
				for (int q = 0; q < k; ++q) {
					for (int j = k; j < n; ++j) {
						G_pre_computed[i](q, j) = Hs(j - k, q);
					}
				}
				//cout << "Gs" << Gs;
			}
		}

		//cout << "permutation_pre_computed" << permutation_pre_computed;
		//cout << "G_pre_computed" << G_pre_computed;
	}

	bool pre_computed_SGM_decoding(const Matrix<my_double>& r) {

		// use Hamming distance criterion what so ever, to be changed to general ML criterion
		int Hd_stop_criteion = (min_Hamming_distance - 1) / 2;

		for (int i = 0; i < n; ++i) {
			//r_abs[i] = my::abs(r[i]);
			y[i] = r[i] > 0 ? 0 : 1;
		}

		// traverse precomputed SGM
		for (int i = 0; i < num_pre_computed; ++i) {

			Gs = G_pre_computed[i];

			y_Gs = y;
			y_Gs.permute(permutation_pre_computed[i]);


			//c0_Gs = yB_Gs * Gs;
			first_re_encoding();
			//cout << "c0_Gs" << c0_Gs;
			//w_min = soft_Hamming_weight(c0_Gs);
			//cout << "w_min = " << w_min << endl;

			//cout << "my_double_auxiliary_storage::operation_number (2) = " << my_double_auxiliary_storage::operation_number << endl;

			if (c0_Gs.Hamming_distance(y_Gs) < Hd_stop_criteion){

				c_hat = c0_Gs;
				c_hat.permute_back(permutation_pre_computed[i]);
				is_early_termination = true;

				return true;
			}

			// do re-encoding on pre-computed SGM, same to choi's algorithm, but the latter FSD part is replaced by our original OSD
			for (int p = 1; p <= order_pre_computed; ++p) {
				TEP_generator.set_weight(p);

				while (TEP_generator.now[0] >= 0) {

					//cout << "TEP_generator.now" << TEP_generator.now;

					re_encoding(TEP_generator.now);

					//cout << "ce_Gs" << ce_Gs;
					//w_ce_Gs = soft_Hamming_weight(ce_Gs);
					//cout << "w_ce_Gs = " << w_ce_Gs << endl;

					//if (w_ce_Gs < w_min) {
					//	w_min = w_ce_Gs;
					//	cb_Gs = ce_Gs;

					//	if (optimal_termination(i) == true) {
					//		return;
					//	}
					//}

					if (ce_Gs.Hamming_distance(y_Gs) < Hd_stop_criteion) {

						c_hat = ce_Gs;
						c_hat.permute_back(permutation_pre_computed[i]);
						is_early_termination = true;

						return true;
					}

					TEP_generator.next();
				}
			}
		}

		return false;
	}

